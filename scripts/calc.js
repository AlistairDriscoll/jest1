
module.exports = {};

module.exports.addition = function addition(num1, num2) {
    return num1 + num2;
}

module.exports.subtraction = function subtraction(num1, num2) {
    return num1 - num2;
}

module.exports.multiplication = function multiplication(num1, num2) {
    return num1 * num2;
}

module.exports.division = function (num1, num2) {
    return num1 / num2;
}
